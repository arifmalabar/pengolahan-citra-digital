% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
open = guidata(gcbo);
imageHsv = get(open.axes2, 'Userdata');
imHue = imageHsv(:,:,1);
imSat = imageHsv(:,:,2);
imVal = imageHsv(:,:,3);
hue = mean(mean(imHue));
saturation = mean(mean(imSat));
value = mean(mean(imVal));
set(handles.edit4,'string',num2str(hue));
set(handles.edit5,'string',num2str(saturation));
set(handles.edit6,'string',num2str(value));
set(open.figure1,'CurrentAxes',open.axes3);
set(open.axes3,'Userdata',histogram(imHue));
set(open.figure1,'CurrentAxes',open.axes4);
set(open.axes4,'Userdata',histogram(imSat));
set(open.figure1,'CurrentAxes',open.axes5);
set(open.axes5,'Userdata',histogram(imVal));


